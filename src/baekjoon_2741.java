import java.util.Scanner;

public class baekjoon_2741 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        sc.close();

        int i = 1;
        for (i = 1; i <= num; i++) {
            System.out.println(i);
        }
    }
}
